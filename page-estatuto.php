          <div id="cap1">
            <h2 class="title">Estatuto CAFar</h2>
            <p>
              <strong> Art. 1°: </strong>O Centro Acadêmico de Farmácia da Universidade de Brasília, doravante designado CAFar – UnB, tem como sede e foro a capital federativa do Brasil, Brasília – Distrito Federal, Campus Darcy Ribeiro, s/n - Gleba FS/FM - Sala CT - 110/17, Asa Norte, Brasília - Brasil, CEP 70.910-900. É, na forma da lei, uma sociedade civil, sem fins lucrativos, o órgão máximo representativo dos alunos de graduação do curso de Farmácia Integral/Noturno da Faculdade de Ciências da Saúde (FS-UnB), tendo suas atividades designadas pelo presente texto na conformidade em vigor.
            </p>
            <p>
              Parágrafo 1° - O CAFar é um órgão independente de partidos políticos, crenças religiosas, raça, orientações filosóficas, ideologia, sexo e segmentos sociais.
            </p>
            <p>
              Parágrafo 2° - O CAFar não é responsável por atitudes tomadas individualmente por qualquer um de seus membros, salvo com aval do presidente ou do vice.
            </p>
            <p>
              Parágrafo 3° - O CAFar tem prazo indeterminado de duração.
            </p>
            <p>
              Parágrafo 4° - O CAFar fica filiado ao órgão de representação nacional sendo esta a Executiva Nacional dos Estudantes de Farmácia (ENEFAR).
            </p>
          </div>

          <div class="cap2">
            <h3 class="title-secondary title-chapter">Capítulo II</h3>
            <h3 class="title-secondary">DOS PRINCÍPIOS E FINALIDADES</h3>
            <p class="post-title">
              <strong> Art. 2°: </strong>O CAFAR tem por finalidades e objetivos:
            </p>
            <p>
              I - Constituir-se na forma da lei, na entidade representativa dos discentes de graduação do
              curso de Farmácia da FS-UnB, defendendo os interesses e direitos do conjunto desses, sem qualquer distinção e zelando pela diversidade de etnia, cor, religião, nacionalidade, gênero, orientação sexual, cultura, idade, convicção política ou social;
            </p>
            <p>
              II - Lutar e defender o ensino público, gratuito, laico, democrático e de qualidade, em todos os níveis, voltado aos interesses da população brasileira;
            </p>
            <p>
              III - Propugnar pela harmonia entre o corpo docente e discente na resolução de problemas comuns;
            </p>
            <p>
              IV - Organizar, divulgar e incentivar eventos de caráter cultural, científico e social, que visem à complementação e aprimoramento da formação universitária;
            </p>
            <p>
              V - Promover e incentivar atividades que visem o desenvolvimento econômico e cultural da sociedade e pela saúde e bem-estar da população;
            </p>
            <p>
              VI - Contribuir para a integração entre seus membros e destes com o movimento estudantil;
            </p>
            <p>
              VII - Lutar pela implementação de políticas que facilitem a permanência dos estudantes nas universidades;
            </p>
            <p>
              VIII - No âmbito acadêmico, lutar por uma melhoria contínua da qualidade do ensino de graduação em farmácia da Faculdade de Ciências da Saúde da UnB;
            </p>
            <p>
              IX - Valorizar, divulgar e dignificar o curso e a profissão farmacêutica.
            </p>
          </div>

          <div class="cap3">
            <h3 class="title-secondary title-chapter">Capítulo III</h3>
            <h3 class="title-secondary">DOS Membros</h3>
            <p class="post-title">
              <strong> Art. 3°: </strong>O CAFar tem como membro todos os discentes regularmente matriculados no curso de graduação em Farmácia (Integral/Noturno), da Faculdade de Ciências da Saúde, designada FS, da Universidade de Brasília – Campus Darcy Ribeiro.
            </p>
            <p>
              <strong>Art. 4°: </strong>São direitos dos membros:
            </p>
            <p>
              I - Participar por palavra oral ou escrita, em qualquer Comissão, Departamento e instância
              deliberativa do CAFar, com permissão das Diretorias;
            </p>
            <p>
              II - Votar na Assembleia Geral;
            </p>
            <p>
              III - Votar e ser votado para diretoria do CAFar;
            </p>
            <p>
              IV - Participar de toda e qualquer atividade promovida pelo CAFar;
            </p>
            <p>
              V - Livre acesso ao estatuto e qualquer deliberação tomada ou contas justificadas pela
              Diretoria do CAFar.
            </p>

            <p>
              <strong>Art.5°: </strong>São deveres dos membros:
            </p>
            <p>
              I - Respeitar e cumprir as disposições do presente estatuto;
            </p>
            <p>
              II - Zelar pelo patrimônio do CAFar;
            </p>
            <p>
              III - Não se utilizar do patrimônio do CAFAR para fins que denigrem a imagem deste;
            </p>
            <p>
              IV - Respeitar as decisões das instâncias deliberativas do CAFar;
            </p>
            <p>
              V - Comparecer, quando convocado, em todas as Assembleias Gerais.
            </p>
          </div>

          <div class="cap4">
            <h3 class="title-secondary title-chapter">Capítulo IV</h3>
            <h3 class="title-secondary">DA ORGANIZAÇÃO E FUNCIONAMENTO</h3>
            <p class="post-title">
              <strong>Art. 6°: </strong>Compõe o CAFar por ordem decrescente de poder deliberativo:
            </p>
            <p>
              I - Assembleia Geral;
            </p>
            <p>
              II - Diretoria.
            </p>
            <h3 class="title-chapter">Seção I -
              da Assembleia Geral</h3>
            <p>
              <strong> Art. 7°: </strong>A Assembleia Geral dos membros do CAFar é a instância máxima de deliberação do CAFar,
              sendo composta por todos os seus membros, com direito a voz e voto.
            </p>
            <p>
              <strong>Art. 8°: </strong>Compete à Assembleia Geral:
            </p>
            <p>
              I - Dar posse aos membros da Diretoria;
            </p>
            <p>
              II - Destituir os Diretores, garantindo-lhes o direito de defesa;
            </p>
            <p>
              III - Reformular total ou parcialmente o presente Estatuto quand
            </p>
            <p>
              IV - Decidir sobre a extinção do CAFar, observando, no que couber, o presente Estatuto;
            </p>
            <p>
              V - Julgar os recursos interpostos e decidir todas as demais atribuições previstas no presente
              Estatuto;
            </p>
            <p>
              VI - Deliberar sobre casos omissos no presente Estatuto.
            </p>
            <p>
              Parágrafo único - Para as deliberações a que se referem os incisos I, II e III deste artigo é exigido
              deliberação da assembleia especialmente convocada para este fim.
            </p>

            <p>
              <strong>Art. 9°: </strong>A Assembleia Geral, será realizada ordinariamente 1 (uma) vez por ano ou,
              extraordinariamente, sempre que convocada pela Diretoria do CAFar ou por 5% (cinco por cento) dos
              membros do CAFar em abaixo-assinado, observando que não poderá ser em período de férias,
              devendo ser presidida pela Diretoria do CAFar.
            </p>
            <p>
              Parágrafo único - A diretoria terá 15 (quinze) dias para realizá-la se esta for convocada pelos
              membros do CAFar em acordo com as exigências do Art. 9° deste Estatuto.
            </p>
            <p>
              <strong>Art. 10°: </strong>A convocação da Assembleia Ordinária deverá ser feita com antecedência mínima de 5
              (cinco) dias úteis e, da Assembleia Extraordinária com antecedência mínima de 48 (quarenta e oito)
              horas, sempre com pauta previamente definida, devendo ser amplamente divulgada por meios de
              comunicação disponíveis.
            </p>
            <p>
              Parágrafo único - Presidirá a Assembleia Geral o Presidente do CAFar com o Vice-presidente e
              Secretário Geral (ou seus respectivos suplentes) completando a mesa.
            </p>            
            <p>
              <strong>Art. 11°: </strong>As sessões da Assembleia Geral se instalará com presença mínima de seus membros
              presentes. Para votar as deliberações das questões previstas nos incisos I, II, V e VI do Art. 8° serão
              necessárias, em primeira convocação, um quórum mínimo de 15% (cinco por cento) de seus
              membros, não atingindo esse quorum, será feita segunda convocação, 15 (quinze) minutos depois e
              vota-se com o número de membros presentes. Para votar as deliberações das questões previstas nos
              incisos III e IV do Art. 8° serão necessárias, em primeira convocação, um quórum mínimo de 20%
              (cinquenta por cento) mais um do número de membros do CAFar , não atingindo esse quorum, será
              feita segunda convocação, 15 (quinze) minutos depois e vota-se com o número de membros presentes.
            </p>
            <p>
              Parágrafo único - As decisões serão tomadas por maioria simples de votos dos membros do CAFar ,
              verificada por lista de assinatura e contagem manual.
            </p>

            <h3 class="title-chapter">
              Seção II - da Diretoria
            </h3>
            <p>
              <strong>Art. 12°: </strong>A diretoria do CAFar é o orgão subordinado às deliberações da Assembleia Geral conforme
              Art. 6° deste estatuto.
            </p>
            <p>
              Parágrafo 1° - A diretoria eleita terá mandato de 1 (um) ano, podendo ser reeleita por períodos
              equivalentes e subsequentes.
            </p>
            <p>
              Parágrafo 2° - A Diretoria se reunirá ordinariamente e extraordinariamente quando necessário,
              registrando-se a presença de seus membros em livro próprio, assinado, lavrando-se ata de cada
              reunião realizada.
            </p>
            <p>
              Parágrafo 3° - O quorum deliberativo das reuniões da Diretoria é a maioria simples.
            </p>
            <p>
              <strong>Art. 13°: </strong>A diretoria será composta, por membros eleitos na forma deste Estatuto:
            </p>
            <p>I - Presidente (a);</p>
            <p>II - Vice-presidente (a);</p>
            <p>III - Diretoria de Finanças e Patrimônio;</p>
            <p>IV - Diretoria de Graduação e Assuntos Estudantis;</p>
            <p>V - Diretoria de Eventos, Cultura e Extensão;</p>
            <p>VI - Diretoria de Comunicação;</p>
            <p>VII - Diretoria política-estudantil;</p>
            <p>VIII - Secretaria.</p>
            <p>
              Parágrafo único - A Diretoria será eleita por sufrágio direto e universal e voto secreto dos membros
              do CAFar.
            </p>
            <p>
              <strong>Art. 14°: </strong>° Estipular-se-á, na ata de posse, o Presidente, Vice-Presidente e a Diretoria de Finanças e
              Patrimônio para responsabilidades com fins de movimentação do caixa e afins.
            </p>
            <p>
              <strong>Art. 15°: </strong>Nenhum membro da Diretoria do CAFar será remunerado, sob qualquer forma ou pretexto,
              sendo vedada a distribuição de lucros, dividendos ou bonificações aos mesmos.
            </p>
            <p>
              <strong>Art. 16°: </strong>Compete à Diretoria além dos deveres competentes como membros:
            </p>
            <p>
              I - Administrar o CAFar, segundo o disposto neste Estatuto e nas deliberações da Assembleia
              Geral;
            </p>
            <p>
              II - Planejar e viabilizar as finanças da entidade;
            </p>
            <p>
              III - Zelar pelo patrimônio e conservação do CAFar;
            </p>
            <p>
              IV - Convocar eleição para a Diretoria do CAFar;
            </p>
            <p>
              V - Manter os estudantes de farmácia constantemente informados acerca das deliberações e
              das atividades do CAFar;
            </p>
            <p>
              VI - Defender os interesses dos membros do CAFar, perante as instâncias universitárias e
              associações de classe;
            </p>
            <p>
              VII - Ter, no máximo, uma falta por mês em Reuniões Ordinárias, com justificativas sendo
              julgadas pelos outros membros da Diretoria;
            </p>
            <p>
              VIII - Defender os interesses do CAFar acima de seus próprios e respeitar as decisões
              tomadas pela maioria da Diretoria;
            </p>
            <p>
              IX - Realizar as atividades atribuídas à sua diretoria específica, segundo disposto neste
              Estatuto e, caso solicitado, realizar atividades de outra diretoria em caráter de apoio.
            </p>
            <p>
              <strong>Art. 17°: </strong>° São atribuições do Presidente:
            </p>
            <p>
              I - Dirigir as atividades gerais do CAFar;
            </p>
            <p>
              II - Representar ativa e passivamente o CAFar em juízo e fora dele;
            </p>
            <p>
              III - Cumprir e fazer cumprir o presente estatuto, as decisões da Diretoria, das Assembléias
              Gerais e reuniões;
            </p>
            <p>
              IV - Dirigir as Assembléias Gerais e as reuniões da Diretoria do CAFar;
            </p>
            <p>
              V - Assinar junto a Diretoria de Finanças e Patrimônio quaisquer documentos necessários à
              movimentação das contas do CAFar;
            </p>
            <p>
              VI - Garantir junto a Secretaria a redação das atas das reuniões ordinárias e extraordinárias da
              Diretoria e das Assembléias, bem como o seu devido encaminhamento;
            </p>
            <p>
              VII - Assinar editais, avisos, circulares, ofícios e a correspondência oficial do CAFar;
            </p>
            <p>
              VIII - Manter contato com outros grupos e entidades do movimento estudantil e universitário
              dentro e fora da UnB;
            </p>
            <p>
              IX - Contratar serviços para a conservação e melhoria do patrimônio e implantação da sede
              social, quando necessário, mediante opção de 3 (três) orçamentos mínimos salvo em situações de
              compra emergencial.
            </p>

            <p>
              <strong>Art. 18°: </strong>° São atribuições do Vice-presidente:
            </p>
            <p>
              I - Dirigir as atividades gerais do CAFar;
            </p>
            <p>
              II - Representar ativa e passivamente o CAFar em juízo e fora dele, na ausência do
              Presidente;
            </p>
            <p>
              III - Cumprir e fazer cumprir o presente estatuto, as decisões da Diretoria, das Assembléias
              Gerais e reuniões;
            </p>
            <p>
              IV - Dirigir as Assembleias Gerais e as reuniões da Diretoria do CAFar;
            </p>
            <p>
              V - Na ausência do Presidente, garantir junto a Secretaria a redação das atas das reuniões
              ordinárias e extraordinárias das Diretorias e das Assembleias, bem como o seu devido
              encaminhamento;
            </p>
            <p>
              VI - Manter contato com outros grupos e entidades do movimento estudantil e universitário
              dentro e fora da UnB;
            </p>
            <p>
              VII - Contratar serviços para a conservação e melhoria do patrimônio e implantação da sede
              social, quando necessário, mediante opção de 3 (três) orçamentos mínimos.
            </p>
            <p>
              <strong>Art. 19°: </strong> São atribuições da Diretoria de Finanças e Patrimônio:
            </p>
            <p>
              I - Ter sob sua responsabilidade os recursos financeiros em geral, todos os bens e valores
              pertencentes ao CAFar;
            </p>
            <p>
              II - Controlar a movimentação financeira do CAFar;
            </p>
            <p>
              III - Efetuar pagamentos e recebimentos de verbas, doações, contribuições ou legados,
              devidamente comprovados, em nome do CAFar, que porventura lhe sejam destinados;
            </p>
            <p>
              IV - Manter atividades em banco (s), inclusive abrir e encerrar contas-corrente e de poupança;
            </p>
            <p>
              V - Lançar todo movimento de receitas e despesas em livros apropriados com a devida
              comprovação;
            </p>
            <p>
              VI - Elaborar balancetes mensais, balanços anuais, prestação de contas e eventuais relatórios,
              de acordo com a legislação em vigor;
            </p>
            <p>
              VII - Rubricar livros contábeis do CAFar e assinar os respectivos termos de abertura e
              encerramento;
            </p>
            <p>
              VIII - Prestar contas perante a Diretoria e Assembleia Geral, tornando-as públicas para todos
              os membros;
            </p>
            <p>
              IX - Representar ativa e passivamente o CAFar em juízo e fora dele, na ausência do
              Presidente e do Vice-presidente;
            </p>
            <p>
              X - Cumprir e fazer cumprir o presente Estatuto.
            </p>
            <p>
              <strong>Art. 20°: </strong>São atribuições da Diretoria de Graduação e Assuntos Estudantis:
            </p>
            <p>
              I - Organizar semestralmente a Avaliação e Discussão de Curso em local e horário que
              permita a participação de todos os estudantes de farmácia;
            </p>
            <p>
              II - Dirigir todas as atividades relacionadas ao ensino de graduação juntamente com a
              representação discente, junto aos orgãos colegiados da UnB;
            </p>
            <p>
              III - Auxiliar os estudantes de Farmácia nos procedimentos relacionados à matrícula em
              disciplinas do curso;
            </p>
            <p>
              IV - Promover intercâmbios entre IES;
            </p>
            <p>
              V - Envolver-se com todos os assuntos estudantis a eles trazidos, buscando atender aos
              problemas dos discentes de forma a orientar os estudantes e solucionar impasses, podendo estes
              problemas serem relacionados a assuntos acadêmicos individuais ou coletivo, questões de sala de
              aula, professores, departamentos, direção, outros cursos ou qualquer assunto do âmbito estudantil;
            </p>
            <p>
              VI - Cumprir e fazer cumprir o presente Estatuto.
            </p>

            <p>
              <strong>Art. 21°: </strong>São atribuições da Diretoria de Cultura, Eventos e Extensão:
            </p>
            <p>
              I - Promover eventos e discussões sobre a extensão no curso de farmácia e na UnB que
              busquem o aprimoramento da prática da extensão, o intercâmbio entre projetos de extensão no curso
              de farmácia e a participação ativa dos estudantes nesses projetos
            </p>
            <p>
              II - Promover cursos de aperfeiçoamento técnico-científico;
            </p>
            <p>
              III - Desenvolver e fomentar a criação artística e cultural entre os estudantes, criando projetos
              e atividades diversas nessas áreas;
            </p>
            <p>
              IV - Promover e incentivar a divulgação dos trabalhos de cunho artístico do corpo discente;
            </p>
            <p>
              V - Organizar um programa de atividades sociais para recreação dos alunos de graduação do
              curso de farmácia, compreendendo festas, excursões, festivais, etc;
            </p>
            <p>
              VI - Favorecer a integração entre alunos, funcionários e professores do Departamento de
              Farmácia da Faculdade de Ciências da Saúde da UnB;
            </p>
            <p>
              VII - Organizar confraternizações e outros eventos realizados pelo CAFar;
            </p>
            <p>
              VIII - Incentivar e auxiliar na participação dos alunos em congressos visando o
              aprimoramento profissional
            </p>
            <p>
              IX - Organizar a recepção dos calouros;
            </p>
            <p>
              X - Cumprir e fazer cumprir o presente Estatuto
            </p>

            <p>
              <strong>Art. 22°: </strong>° São atribuições da Diretoria de Comunicação:
            </p>
            <p>
              I - Criar condições para publicação de informativos, jornais e panfletos do CAFar;
            </p>
            <p>
              II - Divulgar eventos, debates e confraternizações que venham a ser promovidos ou tenham o
              apoio do CAFAR;
            </p>
            <p>
              III - Manter relações com a mídia estudantil e popular, buscando correspondência e
              colaboração com esta;
            </p>
            <p>
              IV - Gerenciar e manter atualizada as redes sociais do CAFar;
            </p>
            <p>
              V - Divulgar todas as deliberações do CAFar;
            </p>
            <p>
              VI - Cumprir e fazer cumprir o presente Estatuto.
            </p>

            <p>
              <strong>Art. 23°: </strong>São atribuições da Diretoria de Política Estudantil;
            </p>
            <p>
              I - Manter relações e comunicação com as entidades representativas profissionais da classe
              farmacêutica no DF;
            </p>
            <p>
              II - Associar-se às atividades envolvidas pelo movimento estudantil da farmácia, centradas na
              Executiva Nacional dos Estudantes de Farmácia (ENEFAR) e suas subsidiárias;
            </p>
            <p>
              III - Fazer inter-relação entre os estudantes de farmácia e demais estudantes e organizações
              estudantis da UnB;
            </p>
            <p>
              IV - Trabalhar para que se mantenha interesse por parte dos estudantes de farmácia os
              assuntos tratados pela diretoria;
            </p>
            <p>
              V - Promover debates, palestras e atividades que visem o aprimoramento do senso crítico
              político-social dos estudantes do curso de farmácia da UnB e da categoria farmacêutica como um
              todo;
            </p>
            <p>
              VI - Cumprir e fazer cumprir o presente Estatuto.
            </p>

            <p>
              <strong>Art. 24°: </strong>São atribuições da Secretaria:
            </p>
            <p>
              I - Superintender todo o movimento da secretaria;
            </p>
            <p>
              II - Redigir e expedir a correspondência oficial do CAFar
            </p>
            <p>
              III - Garantir a redação de atas das reuniões ordinária e extraordinárias da Diretoria e das
              Assembléias, bem como o seu devido encaminhamento;
            </p>
            <p>
              IV - Zelar pelas atas de todas as reuniões ordinárias e extraordinárias da Diretoria e da
              Assembléia;
            </p>
            <p>
              V - Elaborar as pautas, juntamente com a presidência, das reuniões ordinárias e
              extraordinárias da Diretoria e das Assembléias, bem como o seu devido encaminhamento;
            </p>
            <p>
              VI - Zelar e assinar junto a Presidência toda a documentação relacionada ao CAFar
            </p>
            <p>
              VII - Cumprir e fazer cumprir o presente Estatuto.
            </p>

            <p>
              <strong>Art. 25°: </strong>O CAFar tem obrigação, salvo em situações extremas, de estar representado em toda e
              qualquer reunião do Movimento Estudantil de Farmácia (MEF), regionalmente (COREF e EREF) ou
              nacionalmente (CONEF E ENEF). O CAFar deve mandar custeados o Presidente ou Vice-presidente
              ou ainda, na ausência desses dois, o Diretor político-estudantil. O custeio envolve passagem e diárias
              mediante reembolsos através de recibos.
            </p>

            <p>
              <strong>Art. 26°: </strong>Da demissão e exclusão dos membros da Diretoria:
            </p>
            <p>
              É direito dos membros da Diretoria demitir-se quando julgar necessário, protocolando junto a
              Secretaria seu pedido de demissão
            </p>
            <p>
              A exclusão do membro se dará nas seguintes questões:
            </p>
            <p>
              I - Grave violação do Estatuto;
            </p>
            <p>
              II - Difamar o CAFar, seus membros ou objetos;
            </p>
            <p>
              III - Atividades que contrariem decisões de Assembléias;
            </p>
            <p>
              IV - Conduta duvidosa, atos ilícitos ou imorais.
            </p>
            <p>
              Parágrafo único - A perda da qualidade de membro será determinada pela Diretoria, cabendo sempre
              recurso à Assembléia Geral.
            </p>
          </div>

          <div class="cap5">
            <h3 class="title-secondary title-chapter">Capítulo V</h3>
            <h3 class="title-secondary">DAS ELEIÇÕES</h3>
            <p class="post-title">
              <strong>Art. 27°: </strong>As eleições para a constituição da Diretoria serão realizadas no mês de Setembro de cada
              ano, cabendo à Diretoria fixar o dia e ao Presidente convocar as eleições através de indicação da
              Comissão Eleitoral, composta de pelo menos 2 (dois) membros do CAFar com matrícula ativa e que
              não estiverem concorrendo em chapa, com antecedência mínima de 30 (trinta) dias antes das eleições.
            </p>
            <p>
              Parágrafo único - A Comissão Eleitoral será responsável por todo o processo eleitoral.
            </p>
            <p>
              <strong>Art. 28°: </strong>A Comissão Eleitoral será responsável por lançar edital que deverá ser publicado em local
              visível e de ampla divulgação, com antecedência mínima de 15 (quinze) dias às eleições.
            </p>
            <p>
              <strong>Art. 29°: </strong>A inscrição das chapas será feita junto a Comissão Eleitoral, por meio de e-mail, com
              formulário de inscrição contendo o nome da chapa, nome de cada membro, indicando qual o cargo
              pretendido, com assinatura e os comprovantes de matrícula de todos os membros da chapa em anexo.
            </p>
            <p>
              Parágrafo 1° - Poderão concorrer às eleições todos os alunos de graduação em farmácia, do campus
              Darcy Ribeiro, que estiverem com matrícula ativa no período de eleição e que não estiverem na
              situação de provável formando, não podendo figurar nomes em duplicata na mesma ou diferentes
              chapas;
            </p>
            <p>
              Parágrafo 2° - É permitida a reeleição para o mesmo cargo de qualquer membro da Diretoria ou de
              totalidade;
            </p>
            <p>
              Parágrafo 3° - Podem concorrer tantas chapas quanto preencherem as exigências deste Estatuto.
            </p>
            <p>
              <strong>Art. 30°: </strong>A Comissão Eleitoral deve despachar os pedidos de inscrição de todas as chapas até 10 (dez)
              dias antes das eleições.
            </p>
            <p>
              Parágrafo único- A Comissão Eleitoral só poderá rejeitar a inscrição da chapa se esta não atender às
              disposições deste Estatuto e do Edital.
            </p>
            <p>
              <strong>Art. 31°: </strong>As eleições deverão ser coordenadas, acompanhadas e apuradas pela Comissão Eleitoral em
              exercício, um membro da gestão anterior e mais 1 (um) fiscal de cada chapa concorrente, podendo
              haver substituição a todo tempo do fiscal.
            </p>
            <p>
              <strong>Art. 32°: </strong>As eleições serão realizadas em pelo menos 1 (um) dia de aula letivo, em local a ser
              divulgado pela Comissão Eleitoral.
            </p>
            <p>
              <strong>Art. 33°: </strong>Todo eleitor deverá ter seu nome constante da lista nominal de estudantes a ser expedida
              pela Secretaria do Curso 1 (um) dia antes da eleição.
            </p>
            <p>
              Parágrafo único: No caso do eleitor não constar na lista de votação, seu voto será registrado em
              separado e a Comissão Eleitoral deverá localizar seu vínculo para que este seja contabilizado
            </p>
            <p>
              <strong>Art. 34°: </strong>A apuração dos votos deverá ser feita imediatamente após o término da votação, pela
              Comissão Eleitoral acrescida dos fiscais.
            </p>
            <p>Parágrafo 1º - Serão votos nulos:</p>
            <p>
              I - Os votos rasurados;
            </p>
            <p>
              II - Os votos que tiverem mais de uma indicação de chapa e;
            </p>
            <p>
              III - Os votos que não tiverem a assinatura da Comissão Eleitoral.
            </p>
            <p>
              Parágrafo 2º - Os votos em branco serão contabilizados separadamente, sem valor eleitoral.
            </p>
            <p>
              <strong>Art 35°: </strong>Havendo chapa única, esta será eleita se, e somente se, obter 50% (cinquenta por cento) mais
              1 (um) de aprovação dos votos válidos.
            </p>
            <p>
              Parágrafo único - No caso da chapa única não se eleger, a Assembleia Geral decidirá novas eleições
              com novas inscrições de chapas e a antiga Diretoria ficará em regime provisório
            </p>
            <p>
              <strong>Art. 36°: </strong>Qualquer membro do CAFar pode pedir impugnação da eleição mediante pedido que seja
              fundamentado e analisado em Assembleia Geral.
            </p>
            <p>
              <strong>Art. 37°: </strong>O mandato dos membros da Diretoria inicia-se a partir da posse, que se dá solenemente até 1
              (uma) semana após a apuração dos votos em Assembleia convocada para este fim.
            </p>
          </div>

          <div class="cap6">
            <h3 class="title-secondary title-chapter">Capítulo VI</h3>
            <h3 class="title-secondary">DA PRESTAÇÃO DE CONTAS</h3>
            <p class="post-title">
              <strong>Art. 38°: </strong>A Diretoria prestará contas em Assembleia Geral no ato da posse da nova Diretoria ou
              quando solicitada em Assembleia Geral.
            </p>
            <p>
              <strong>Art. 39°: </strong>Serão aceitos como justificativas de gastos os seguintes documentos:
            </p>
            <p>
              I - Nota fiscal devidamente preenchida e sem rasuras;
            </p>
            <p>
              II - Recibos fiscais devidamente preenchidos com nome, endereço, telefone, CPF do emitente
              e descrição do serviço. Por cada mês, o total de gastos declarados por recibo deverá ser igual ou
              inferior a 50% (cinquenta por cento) do salário mínimo federal no mês em questão.
            </p>
          </div>

          <div class="cap7">
            <h3 class="title-secondary title-chapter">Capítulo VII</h3>
            <h3 class="title-secondary">DO PATRIMÔNIO E OS RECURSOS FINANCEIROS</h3>
            <p class="post-title">
              <strong>Art. 40°: </strong>O patrimônio do CAFar é constituído por bens e imóveis e direitos que possui ou venha a
              possuir, por aquisição ou doação.
            </p>
            <p>
              Parágrafo único - Os bens e direitos do CAFar serão utilizados ou aplicados exclusivamente para a
              consecução de seus objetivos.
            </p>
            <p>
              <strong>Art. 41°: </strong>Os recursos financeiros do CAFar são constituídos por:
            </p>
            <p>
              I - Doações, auxílios, subvenções e legado de origens legais que lhe venha a ser feito ou
              concedido pela Universidade de Brasília, entidades públicas ou privadas ou ainda pessoas físicas.
            </p>
            <p>
              <strong>Art. 42°: </strong>Os bens patrimoniais móveis e imóveis do CAFar são em princípio, inalienáveis, sendo que
              sua venda ou troca ou doações deve passar por aprovação de Assembléia Geral especialmente
              convocada para esse fim.
            </p>
            <p>
              <strong>Art. 43°: </strong>Em caso de dissolução do CAFar, aprovada pela Assembléia Geral, proceder-se-á ao
              levantamento de patrimônio, que será destinado a outras instituições legalmente constituídas, sem fins
              lucrativos e com objeto social semelhante aos da Associação.
            </p>
            <p>
              Parágrafo único - A receita do CAFar é proveniente da venda de brindes, tais como camisetas,
              chaveiros, jalecos, outros equipamentos de segurança individual, atividades, festas e festivais, entre
              outros.
            </p>
            <p>
              <strong>Art. 44°: </strong>O uso dos recursos do CAFar deve, obrigatoriamente ter autorização, aprovação e
              conhecimento por parte da diretoria.
            </p>
            <p>
              <strong>Art. 45: </strong>O zelo pelo patrimônio do CAFar é dever de todos e responsabilidade da Diretoria que, em
              caso de dano material poderá convocar reunião entre seus membros e deliberar punição e cobrança de
              ressarcimento por parte do causador, ou, caso não seja descoberto, apresentar orçamentos e solicitar o
              rateio entre os membros.
            </p>
            <p>
              <strong>Art. 46°: </strong>O CAFar não se responsabiliza por má conduta dos membros em relação ao patrimônio
            </p>
          </div>

          <div class="cap8">
            <h3 class="title-secondary title-chapter">Capítulo VIII</h3>
            <h3 class="title-secondary">DA REPRESENTAÇÃO DISCENTE</h3>
            <p class="post-title">
              <strong>Art. 47°: </strong>A representação discente junto aos órgãos colegiados, câmaras de graduação e núcleo
              docente estruturante do departamento de farmácia da FS, será eleita diretamente por maioria dos votos
              simples em reunião a ser feita pela Diretoria logo após a posse, devendo ter 1 (um) representante para
              o curso diurno e 1 (um) representante para o curso noturno.
            </p>
            <p>
              Parágrafo 1° - Não será aceita indicação de representantes que não sejam membros da Diretoria do
              CAFar
            </p>
            <p>
              Parágrafo 2° - A perda, por qualquer motivo, em qualquer tempo, da condição a que se refere o
              parágrafo anterior deste artigo, implicará na perda do mandato.
            </p>
            <p>
              <strong>Art. 48°: </strong>Cabe à representação do CAFar nos órgãos colegiados, câmaras de graduação e núcleo
              docente estruturante do departamento de farmácia da FS:
            </p>
            <p>
              I - Estar presente e representar o CAFar em todas as reuniões dos órgãos colegiados, câmaras
              de graduação e núcleo docente estruturante do departamento de farmácia da FS informando os
              estudantes dos assuntos ali tratados, suas decisões e as posições defendidas pelas representações
              estudantis e compartilhar com o CAFar e todos os alunos nos meios de comunicação pertinentes as
              pautas e atas das reuniões dos órgãos assim que estas forem aprovadas por todos.
            </p>
            <p>
              II - Submeter-se a todas as decisões do CAFar tomadas nas instâncias de deliberação da
              entidade e defender os interesses deste independentemente de interesses pessoais.
            </p>
            <p>
              III - Não utilizar os benefícios e informações obtidas pelo cargo em benefício próprio ou de
              outro membro do CAFar;
            </p>
            <p>
              IV - Estar disposto e aberto a auxiliar os alunos do departamento de farmácia da FS em
              questões referentes à graduação.
            </p>
            <p>
              V - Respeitar e zelar pela diversidade de etnia, cor, religião, nacionalidade, gênero, orientação
              sexual, cultura, idade, convicção política ou social e denunciar casos em que houver desrespeito.
            </p>
            <p>
              <strong>Art. 49°: </strong>Caso haja o não cumprimento de qualquer atribuição disposta no Art. 47°, os membros da
              Diretoria poderão convocar uma reunião da Diretoria para destituir o Representante Discente do cargo
              e eleger outro membro da Diretoria para o cargo em questão.
            </p>
          </div>

          <div class="cap9">
            <h3 class="title-secondary title-chapter">Capítulo IX</h3>
            <h3 class="title-secondary">DAS PENALIDADES E SUAS APLICAÇÕES</h3>
            <p class="post-title">
              <strong>Art. 50°: </strong>Os membros poderão ser:
            </p>
            <p>
              I - Advertidos;
            </p>
            <p>
              II - Afastados;
            </p>
            <p>
              III - Excluídos do quadro de membro se praticar ato contrário à Lei, à moral e à ética,
              infringindo os deveres contidos neste Estatuto.
            </p>
            <p>
              <strong>Art. 51°: </strong>A exclusão do membro só é admissível havendo justa causa, obedecido ao disposto neste
              Estatuto e somente ocorrerá se for reconhecida e comprovada a existência de graves motivos,
              apontados em decisão fundamentada pela Diretoria do CAFar, que deverá votar com a maioria
              absoluta dos presentes em Assembleia especialmente convocada para este fim, ou ainda quando o
              mesmo manifestar interesse de livre e espontânea vontade.
            </p>
            <p>
              Parágrafo 1º - Entende-se por motivos graves, entre outros:
            </p>
            <p>
              I - Não cumprir com as obrigações que lhe forem atribuídas e que comprometam moralmente
              o CAFAR, prejudicando sua imagem e reputação;
            </p>
            <p>
              II - Praticar atos que comprometam moralmente o CAFar, prejudicando sua imagem e
              reputação;
            </p>
            <p>
              III - Proceder com má administração de recursos;
            </p>
            <p>
              IV - Infringir as demais normas previstas neste Estatuto e na lei.
            </p>
            <p>
              Parágrafo 2º - À Diretoria do CAFar, os motivos de exclusão, além do disposto no parágrafo primeiro
              do Art. 51°, incluem-se os motivos dispostos no Art. 26° deste Estatuto.
            </p>
            <p>
              Parágrafo 3º - Da decisão do órgão que efetivar a exclusão do membro caberá sempre recurso
              fundamentado à Assembléia, no prazo de 15 (quinze) dias da comunicação da decisão ao membro
              excluído, por meio de requerimento escrito dirigido ao Presidente do CAFar.
            </p>
          </div>

          <div class="cap10">
            <h3 class="title-secondary title-chapter">Capítulo X</h3>
            <h3 class="title-secondary">DAS DISPOSIÇÕES GERAIS E TRANSITÓRIAS</h3>
            <p class="post-title">
              <strong>Art. 52°: </strong>O CAFar terá seu Estatuto reformulado, todo ou em partes, de acordo com o disposto no
              Art. 11° deste Estatuto ou será dissolvido por decisão dos membros em Assembléia convocada
              extraordinariamente para este fim, quando se julgar necessário ou quando se tornar impossível a
              continuidade de suas atividades, com a aprovação de 50% (cinquenta por cento) mais 1 (um) do
              número total de membros do CAFar.
            </p>
            <p>
              <strong>Art. 53°: </strong>Os casos omissos serão resolvidos pela Diretoria do CAFar.
            </p>
            <p>
              Parágrafo único: A decisão da Diretoria estará sujeita a reforma pela Assembleia Geral, conforme Art.
              8°, item VI
            </p>
            <p>
              <strong>Art. 54°: </strong>Os membros não respondem solidária ou subsidiariamente pelas obrigações construídas pelo
              CAFar.
            </p>
            <p>
              <strong>Art. 55°: </strong>Este estatuto entra em vigor em (DATA), no calendário oficial adotado pela República
              Federativa do Brasil e revoga-se dispositivos anteriores.
            </p>
          </div>

